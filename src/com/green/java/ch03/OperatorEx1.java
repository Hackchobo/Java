package com.green.java.ch03;

public class OperatorEx1 {
    public static void main(String[] args) {
        int i=5;
        System.out.println(i);
        i++;                            // 숫자만 가능한 식이다.
        i++;
        System.out.println(i);

        i = 5;
        ++i;
        System.out.println(i);
    }

}
