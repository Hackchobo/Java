package com.green.java.ch04;

public class ForQuiz5 {
    public static void main(String[] args) {
        int gugudan = 2;

        for (int i = 1; i <= 9; i++) {
            System.out.printf("%d * %d = %d \n", gugudan, i, gugudan * i);
            System.out.println(gugudan +"x"+i +"="+gugudan*i);
        }
    }
}
